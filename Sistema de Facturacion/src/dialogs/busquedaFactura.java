    public void buscarSecuencial(){
        conexion_mysql cn=new conexion_mysql();
        Connection cc=cn.conectar();
        String sql="";
        sql="select NUM_SEC FROM secuencial";
        try {
            Statement ps=cc.createStatement();
            ResultSet rs=ps.executeQuery(sql);
            while(rs.next()){
                lblSecuencial.setText(rs.getString("NUM_SEC"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(busquedaFactura.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    public void busqueda(String secuencial){
        String[] datosF=new String[4];
        String[] titulos={"Numeraci√≥n","Secuencial","Ced. Cliente","Nombres y Apellidos cliente"};
        modelo=new DefaultTableModel(null, titulos);
        conexion_mysql cc=new conexion_mysql();
        Connection cn=cc.conectar();
        String sql="";
        sql="select e.sec_fac,e.num_fac,e.clie_fac,CONCAT_WS(' ',cli.NOM_CLI, cli.NOM_CLI1, cli.ape_cli, cli.ape_cli1) as nombres from clientes cli, encabezadofactura e "
                + "where e.num_fac like '%"+secuencial+"%' and e.clie_fac=cli.CI_CLI";
        //System.out.println(sql);
        try {
            Statement ps=cn.createStatement();
            ResultSet rs=ps.executeQuery(sql);
            while(rs.next()){
                datosF[0]=rs.getString("sec_fac");
                datosF[1]=rs.getString("num_fac");
                datosF[2]=rs.getString("clie_fac");
                datosF[3]=rs.getString("nombres");
                modelo.addRow(datosF);
            }
            jtblFacturas.setModel(modelo);
        } catch (SQLException ex) {
            Logger.getLogger(busquedaFactura.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

